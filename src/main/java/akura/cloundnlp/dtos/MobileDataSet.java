package akura.cloundnlp.dtos;

public class MobileDataSet {
    private String name;
    private String releaseYear;
    private String volume_mm_3;
    private String weight_g;
    private String screen_size_in;
    private String total_resolution;
    private String OSType;
    private String number_of_cores;
    private String core_clock_rate_gHz;
    private String ram;
    private String primary_camera_MP;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getReleaseYear() {
        return releaseYear;
    }

    public void setReleaseYear(String releaseYear) {
        this.releaseYear = releaseYear;
    }

    public String getVolume_mm_3() {
        return volume_mm_3;
    }

    public void setVolume_mm_3(String volume_mm_3) {
        this.volume_mm_3 = volume_mm_3;
    }

    public String getWeight_g() {
        return weight_g;
    }

    public void setWeight_g(String weight_g) {
        this.weight_g = weight_g;
    }

    public String getScreen_size_in() {
        return screen_size_in;
    }

    public void setScreen_size_in(String screen_size_in) {
        this.screen_size_in = screen_size_in;
    }

    public String getTotal_resolution() {
        return total_resolution;
    }

    public void setTotal_resolution(String total_resolution) {
        this.total_resolution = total_resolution;
    }

    public String getOSType() {
        return OSType;
    }

    public void setOSType(String OSType) {
        this.OSType = OSType;
    }

    public String getNumber_of_cores() {
        return number_of_cores;
    }

    public void setNumber_of_cores(String number_of_cores) {
        this.number_of_cores = number_of_cores;
    }

    public String getCore_clock_rate_gHz() {
        return core_clock_rate_gHz;
    }

    public void setCore_clock_rate_gHz(String core_clock_rate_gHz) {
        this.core_clock_rate_gHz = core_clock_rate_gHz;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getPrimary_camera_MP() {
        return primary_camera_MP;
    }

    public void setPrimary_camera_MP(String primary_camera_MP) {
        this.primary_camera_MP = primary_camera_MP;
    }
}
